package Entities;

/**
 * The Enum ItemType represents the types an item can belong to.
 */
public enum ItemType {
	
	/** Bouquet type. */
	//for pre_defined catalog//
	bouquet,
	
	/** Bridal bouquet type. */
	bridal_bouquet,
	
	/** Pot type. */
	pot,
	
	/** Flower type. */
	//for custom catalog//
	flower,
	
	/** Seed type. */
	seed,
	
	/** Branch type. */
	branch,
	
	/** Soil type. */
	soil,
}
