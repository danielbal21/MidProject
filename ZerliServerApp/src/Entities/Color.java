package Entities;



/**
 * The Enum Color represents the available colors for a product.
 */
public enum Color {
	
	/** The color red. */
	red,
	
	/** The color blue. */
	blue,
	
	/** The color green. */
	green,
	
	/** The color white. */
	white,
	
	/** The color black. */
	black,
	
	/** The color pink. */
	pink,
	
	/** The color purple. */
	purple,
	
	/** The color orange. */
	orange,
	
	/** The color yellow. */
	yellow,
	
	/** The color brown. */
	brown,
	
	/** No specific color. */
	non_color,
}
